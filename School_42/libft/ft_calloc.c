#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	int		i;

	i = count * size;
	ptr = malloc(i);
	if (!ptr)
		return (0);
	ft_bzero(ptr, i);
	return (ptr);
}
