#include "libft.h"

int	ft_atoi(const char *str)
{
	int	conv;
	int	sign;

	conv = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		conv = ((*str - 48) + (conv * 10));
		str++;
	}
	return (conv * sign);
}
